#!/usr/bin/env node
'use strict';

const fs = require('node:fs');
const path = require('node:path');

const PROJECT_ROOT = path.resolve(__dirname, '..');
const TARGET_ROOT = path.join(PROJECT_ROOT, 'media', 'material-icons');
const TARGET_ICONS_DIR = path.join(TARGET_ROOT, 'icons');
const TARGET_ICONS_JS = path.join(PROJECT_ROOT, 'media', 'icons.js');

function readJson(filePath) {
    return JSON.parse(fs.readFileSync(filePath, 'utf8'));
}

function resolvePackageRoot(packageName) {
    try {
        return path.dirname(require.resolve(`${packageName}/package.json`, { paths: [PROJECT_ROOT] }));
    } catch (error) {
        console.error(`Cannot find ${packageName}. Run \`npm install\` first.`);
        process.exitCode = 1;
        throw error;
    }
}

function ensureDirectory(dirPath) {
    fs.mkdirSync(dirPath, { recursive: true });
}

function copyFile(source, target) {
    ensureDirectory(path.dirname(target));
    fs.copyFileSync(source, target);
}

function iconFileFromDefinition(manifest, iconId) {
    const definition = manifest.iconDefinitions && manifest.iconDefinitions[iconId];
    if (!definition || !definition.iconPath) return null;
    return path.basename(definition.iconPath);
}

function mapIconAssociations(manifest, section) {
    const result = {};
    for (const [key, iconId] of Object.entries(section || {})) {
        const fileName = iconFileFromDefinition(manifest, iconId);
        if (fileName) result[key.toLowerCase()] = fileName;
    }
    return result;
}

function buildIconMap(manifest) {
    return {
        defaultFile: iconFileFromDefinition(manifest, manifest.file) || 'file.svg',
        defaultFolder: iconFileFromDefinition(manifest, manifest.folder) || 'folder.svg',
        defaultFolderOpen: iconFileFromDefinition(manifest, manifest.folderExpanded) || 'folder-open.svg',
        fileNames: mapIconAssociations(manifest, manifest.fileNames),
        fileExtensions: mapIconAssociations(manifest, manifest.fileExtensions),
        folderNames: mapIconAssociations(manifest, manifest.folderNames),
        folderNamesExpanded: mapIconAssociations(manifest, manifest.folderNamesExpanded),
    };
}

function renderIconsJs(iconMap, packageJson) {
    return `// Webview icon resolver backed by the real Material Icon Theme SVG pack.
//
// Generated by scripts/sync-material-icons.js from ${packageJson.name}@${packageJson.version}.
// Source: ${packageJson.repository && packageJson.repository.url ? packageJson.repository.url.replace(/^git\+/, '') : 'https://github.com/material-extensions/vscode-material-icon-theme'}
// License: ${packageJson.license || 'MIT'}
//
// Public API is intentionally unchanged:
//   window.RepoIcons.getNodeIconSvg(name, isDirectory, isExpanded)
//   window.RepoIcons.resolveSpec(name)

(function () {
    const MATERIAL_ICONS = ${JSON.stringify(iconMap)};

    const ICON_BASE_URI = (function resolveIconBaseUri() {
        if (typeof window !== 'undefined' && window.REPOTXT_MATERIAL_ICONS_BASE_URI) {
            return String(window.REPOTXT_MATERIAL_ICONS_BASE_URI).replace(/\\/?$/, '/');
        }

        const scriptSrc = document.currentScript && document.currentScript.src;
        try {
            return new URL('./material-icons/icons/', scriptSrc || window.location.href).toString();
        } catch {
            return './material-icons/icons/';
        }
    })();

    function normalizeName(name) {
        return String(name || '').trim().toLowerCase();
    }

    function escapeAttribute(value) {
        return String(value).replace(/[&<>"']/g, (ch) => ({
            '&': '&amp;',
            '<': '&lt;',
            '>': '&gt;',
            '"': '&quot;',
            "'": '&#39;',
        }[ch]));
    }

    function iconUri(iconFile) {
        return ICON_BASE_URI + encodeURIComponent(iconFile);
    }

    function extensionCandidates(lowerName) {
        const candidates = [lowerName];
        for (let i = 0; i < lowerName.length; i += 1) {
            if (lowerName[i] === '.' && i + 1 < lowerName.length) {
                candidates.push(lowerName.slice(i + 1));
            }
        }
        return candidates.sort((a, b) => b.length - a.length);
    }

    function resolveFolderIcon(name, isExpanded) {
        const lowerName = normalizeName(name);
        const expandedIcon = MATERIAL_ICONS.folderNamesExpanded[lowerName];
        const closedIcon = MATERIAL_ICONS.folderNames[lowerName];

        if (isExpanded && expandedIcon) return expandedIcon;
        if (closedIcon) return closedIcon;
        if (expandedIcon) return expandedIcon;
        return isExpanded ? MATERIAL_ICONS.defaultFolderOpen : MATERIAL_ICONS.defaultFolder;
    }

    function resolveFileIcon(name) {
        const lowerName = normalizeName(name);
        if (!lowerName) return MATERIAL_ICONS.defaultFile;

        const byName = MATERIAL_ICONS.fileNames[lowerName];
        if (byName) return byName;

        for (const candidate of extensionCandidates(lowerName)) {
            const byExtension = MATERIAL_ICONS.fileExtensions[candidate];
            if (byExtension) return byExtension;
        }

        return MATERIAL_ICONS.defaultFile;
    }

    function resolveSpec(name, isDirectory, isExpanded) {
        const iconFile = isDirectory
            ? resolveFolderIcon(name, Boolean(isExpanded))
            : resolveFileIcon(name);

        return {
            pack: '${packageJson.name}',
            iconFile,
            uri: iconUri(iconFile),
        };
    }

    function getNodeIconSvg(name, isDirectory, isExpanded) {
        const spec = resolveSpec(name, isDirectory, isExpanded);
        const className = isDirectory ? 'repo-material-icon repo-material-folder-icon' : 'repo-material-icon repo-material-file-icon';
        return '<img class="' + className + '" src="' + escapeAttribute(spec.uri) + '" alt="" draggable="false" loading="lazy">';
    }

    window.RepoIcons = { getNodeIconSvg, resolveSpec };
})();
`;
}

function syncMaterialIcons() {
    const packageRoot = resolvePackageRoot('material-icon-theme');
    const packageJson = readJson(path.join(packageRoot, 'package.json'));
    const manifestPath = path.join(packageRoot, 'dist', 'material-icons.json');
    const sourceIconsDir = path.join(packageRoot, 'icons');

    if (!fs.existsSync(manifestPath)) {
        throw new Error(`Material Icon Theme manifest not found: ${manifestPath}`);
    }
    if (!fs.existsSync(sourceIconsDir)) {
        throw new Error(`Material Icon Theme icons directory not found: ${sourceIconsDir}`);
    }

    const manifest = readJson(manifestPath);
    const iconMap = buildIconMap(manifest);

    fs.rmSync(TARGET_ROOT, { recursive: true, force: true });
    ensureDirectory(TARGET_ICONS_DIR);

    let copiedIcons = 0;
    for (const entry of fs.readdirSync(sourceIconsDir)) {
        if (!entry.endsWith('.svg')) continue;
        copyFile(path.join(sourceIconsDir, entry), path.join(TARGET_ICONS_DIR, entry));
        copiedIcons += 1;
    }

    copyFile(path.join(packageRoot, 'LICENSE'), path.join(TARGET_ROOT, 'LICENSE'));

    const sourceMetadata = {
        name: packageJson.name,
        version: packageJson.version,
        license: packageJson.license,
        npm: `https://www.npmjs.com/package/${packageJson.name}`,
        repository: packageJson.repository && packageJson.repository.url ? packageJson.repository.url.replace(/^git\+/, '') : undefined,
        manifest: 'dist/material-icons.json',
        icons: 'icons/*.svg',
        vendoredFor: 'RepoTxt webview file and folder icons',
    };

    fs.writeFileSync(
        path.join(TARGET_ROOT, 'source.json'),
        `${JSON.stringify(sourceMetadata, null, 2)}\n`,
        'utf8',
    );

    fs.writeFileSync(
        path.join(TARGET_ROOT, 'NOTICE.md'),
        `Material Icon Theme assets\n\nSource: ${sourceMetadata.repository || 'https://github.com/material-extensions/vscode-material-icon-theme'}\nNPM package: ${packageJson.name}\nVersion: ${packageJson.version}\nLicense: ${packageJson.license}\n\nThe SVG assets in ./icons are vendored from the ${packageJson.name} package.\nRun \`npm run sync:icons\` to refresh them from node_modules.\n`,
        'utf8',
    );

    fs.writeFileSync(TARGET_ICONS_JS, renderIconsJs(iconMap, packageJson), 'utf8');

    console.log(`Synced ${packageJson.name}@${packageJson.version}`);
    console.log(`Copied ${copiedIcons} SVG icons to ${path.relative(PROJECT_ROOT, TARGET_ICONS_DIR)}`);
    console.log(`Regenerated ${path.relative(PROJECT_ROOT, TARGET_ICONS_JS)}`);
}

try {
    syncMaterialIcons();
} catch (error) {
    if (process.exitCode !== 1) {
        console.error(error instanceof Error ? error.message : error);
        process.exitCode = 1;
    }
}
